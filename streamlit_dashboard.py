def plotly_pie(df, names_col, values_col, title):
    fig = px.pie(
        df, names=names_col, values=values_col, title=title,
        color_discrete_sequence=px.colors.sequential.Darkmint,
        hole=0.35,
    )
    fig.update_traces(textposition="outside", textinfo="percent+label")
    fig.update_layout(
        paper_bgcolor="rgba(0,0,0,0)",
        plot_bgcolor ="rgba(0,0,0,0)",
        font_color  =TEXT_DARK,
        title_font  =dict(family="Playfair Display", size=16, color=DARK_OLIVE),
        margin      =dict(t=60, b=20, l=20, r=20),
        legend      =dict(font=dict(size=11)),
    )
    return fig


def plotly_bar(df, x_col, y_col, title, color=DARK_OLIVE):
    fig = px.bar(
        df, x=x_col, y=y_col, title=title,
        color_discrete_sequence=[color],
        text=y_col,
    )
    fig.update_traces(texttemplate="%{text:.1f}%", textposition="outside")
    fig.update_layout(
        paper_bgcolor="rgba(0,0,0,0)",
        plot_bgcolor ="rgba(0,0,0,0)",
        font_color  =TEXT_DARK,
        title_font  =dict(family="Playfair Display", size=16, color=DARK_OLIVE),
        xaxis_tickangle=-35,
        margin=dict(t=60, b=60, l=20, r=20),
        yaxis_title="Allocation %",
    )
    return fig


def plotly_line(df, x_col, y_col, title):
    fig = go.Figure()
    fig.add_trace(go.Scatter(
        x=df[x_col], y=df[y_col],
        mode="lines+markers",
        line=dict(color=DARK_OLIVE, width=2.5),
        marker=dict(size=6, color=ACCENT),
        fill="tozeroy",
        fillcolor=f"rgba(59,68,54,0.08)",
    ))
    fig.update_layout(
        title=dict(text=title, font=dict(family="Playfair Display", size=16, color=DARK_OLIVE)),
        paper_bgcolor="rgba(0,0,0,0)",
        plot_bgcolor ="rgba(0,0,0,0)",
        font_color  =TEXT_DARK,
        xaxis_title =x_col,
        yaxis_title ="Portfolio Value (KES)",
        margin      =dict(t=60, b=40, l=20, r=20),
        hovermode   ="x unified",
    )
    return fig


