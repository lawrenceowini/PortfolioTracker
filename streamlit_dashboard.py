        sector_pct   = (sector_alloc / total * 100).reset_index()
        sector_pct.columns = ["Sector", "Sector Weight %"]
        sector_viols = sector_pct[sector_pct["Sector Weight %"] > sector_lim].copy()
        sector_viols["Limit %"]  = sector_lim
        sector_viols["Excess %"] = (sector_viols["Sector Weight %"] - sector_lim).round(2)
        sector_viols["Sector Weight %"] = sector_viols["Sector Weight %"].round(2)

        # Score: 10 pts flat per violation + 1 pt per % of excess
        penalty = 0
        for _, row in asset_viols.iterrows():
            penalty += 10 + float(row["Excess %"])
        for _, row in sector_viols.iterrows():
            penalty += 10 + float(row["Excess %"])

        score = max(0, min(100, round(100 - penalty)))
        return score, asset_viols.reset_index(drop=True), sector_viols.reset_index(drop=True)

