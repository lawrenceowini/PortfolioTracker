SHEET_NAMES = {
    "dashboard" : "Dashboard1",
    "holdings"  : "Holdings",
    "nse"       : "NSE_Prices",
    "dividends" : "Dividends",
    "history"   : "Performance History",
    "tx"        : "Transactions",
}

@st.cache_data(show_spinner=False)
def load_workbook_sheets(file_bytes_or_path, is_path=False):
    """Load all relevant sheets from the output Excel workbook."""
    sheets = {}
    try:
        src = file_bytes_or_path if is_path else file_bytes_or_path
        xl  = pd.ExcelFile(src)
        for key, name in SHEET_NAMES.items():
            if name in xl.sheet_names:
                sheets[key] = pd.read_excel(src, sheet_name=name)
            else:
                sheets[key] = pd.DataFrame()
    except Exception as e:
        st.error(f"Could not read workbook: {e}")
    return sheets


